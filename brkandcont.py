#break and continue
print("using break")
for i in[1,2,3,4,5]:
    if (i==3):
        break
    print(i)

print("out of the loop")
print(i)
    

print("using continue")

for i in[1,2,3,4,5]:
    if (i==3):
        continue
    print(i)    

